# 1. Creating and updating variables
print("--- Variable Updates ---")
score = 10
print(f"Initial score: {score}")

score = score + 5
print(f"After adding 5: {score}")

score += 2  # Shorthand for score = score + 2
print(f"After shorthand += 2: {score}")

# 2. Integer and Arithmetic Operations
print("\n--- Arithmetic Operations ---")
a = 15
b = 4

print(f"Variables: a = {a}, b = {b}")
print(f"Addition (a + b): {a + b}")
print(f"Subtraction (a - b): {a - b}")
print(f"Multiplication (a * b): {a * b}")
print(f"Division (a / b): {a / b}")
print(f"Comparison (a > b): {a > b}")
print(f"Comparison (a < b): {a < b}")
print(f"Comparison (a == b): {a == b}")
print(f"Comparison (a != b): {a != b}")
print(f"Floor Division (a // b): {a // b} (rounds down to nearest integer)")
print(f"Remainder/Modulo (a % b): {a % b} (what remains after division)")
print(f"Exponentiation (2 ** 3): {2 ** 3} (2 raised to the power of 3)")
